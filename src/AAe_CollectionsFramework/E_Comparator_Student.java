package AAe_CollectionsFramework;

public class E_Comparator_Student {

    public int age;

    public String name;

    public int weight;

    public E_Comparator_Student(int age, String name, int weight){
        this.age = age;
        this.name = name;
        this.weight = weight;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public int getWeight() {
        return weight;
    }


    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
